% Створення випадкових даних для прикладу
data = randn(100, 3); % Припустимо, що у нас є 100 спостережень та 3 ознаки
% Аналіз та візуалізація даних
subplot(1, 2, 1);
histogram(data(:, 1)); % Візуалізація розподілу першої ознаки
xlabel('Feature 1'); % Підпис для вісі X
ylabel('Frequency'); % Підпис для вісі Y
subplot(1, 2, 2);
scatter(data(:, 2), data(:, 1)); % Відображення залежності між другою та першою ознаками
xlabel('Feature 2'); % Підпис для вісі X
ylabel('Feature 1'); % Підпис для вісі Y Аналіз та візуалізація даних
